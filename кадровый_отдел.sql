CREATE TABLE Должность (
    ID_должности INT PRIMARY KEY,
    Название_должности VARCHAR(255)
);

-- Создание таблицы "Штатное расписание"
CREATE TABLE Штатное_расписание (
    ID_штатной_единицы INT PRIMARY KEY,
    ID_должности INT,
    Количество_ставок DECIMAL(3, 2), -- Предполагаем, что количество ставок может быть дробным, например, 0.5
    Надбавка DECIMAL(10, 2),       -- Денежная сумма надбавки
    Оклад DECIMAL(10, 2),          -- Денежная сумма оклада
    FOREIGN KEY (ID_должности) REFERENCES Должность(ID_должности)
);

-- Создание таблицы "Сотрудники"
CREATE TABLE Сотрудники (
    ID_сотрудника INT PRIMARY KEY,
    Фамилия VARCHAR(255),
    Имя VARCHAR(255),
    Отчество VARCHAR(255),
    Дата_рождения DATE,
    Пол VARCHAR(10), -- Например, "мужской", "женский"
    Семейное_положение VARCHAR(255),
    Адрес_проживания VARCHAR(255),
    Телефон VARCHAR(20),
    Серия_паспорта VARCHAR(20),
    Номер_паспорта VARCHAR(20),
    Где_и_кем_выдан VARCHAR(255),
    Дата_выдачи DATE,
    Место_рождения VARCHAR(255),
    Образование VARCHAR(255),
    Национальность VARCHAR(255)
);

-- Создание таблицы "Приказ о приеме на работу"
CREATE TABLE Приказ_о_приеме_на_работу (
    Номер_приказа INT PRIMARY KEY,
    ID_сотрудника INT,
    Дата_утверждения DATE,
    Количество_ставок DECIMAL(3,2),  --Соответствует штатному расписанию
    ID_штатной_единицы INT,
    FOREIGN KEY (ID_сотрудника) REFERENCES Сотрудники(ID_сотрудника),
    FOREIGN KEY (ID_штатной_единицы) REFERENCES Штатное_расписание(ID_штатной_единицы)
);

-- Создание таблицы "Приказ о переводе на другую должность"
CREATE TABLE Приказ_о_переводе_на_другую_должность (
    Номер_приказа INT PRIMARY KEY,
    ID_сотрудника INT,
    Дата_утверждения DATE,
    ID_штатной_единицы INT,
    FOREIGN KEY (ID_сотрудника) REFERENCES Сотрудники(ID_сотрудника),
    FOREIGN KEY (ID_штатной_единицы) REFERENCES Штатное_расписание(ID_штатной_единицы)
);

-- Создание таблицы "Приказ об увольнении"
CREATE TABLE Приказ_об_увольнении (
    Номер_приказа INT PRIMARY KEY,
    ID_сотрудника INT,
    Дата_утверждения DATE,
    Основание TEXT, --  Для хранения более длинных текстов обоснования
    FOREIGN KEY (ID_сотрудника) REFERENCES Сотрудники(ID_сотрудника)
);

